const DesktopNav = () => {
  return (
    <Stack >
      {NAV_ITEMS.map((navItem) => (
        <Box key={navItem.label}>
          <Popover>
            <PopoverTarget>
              <Box
                p={2}
              >
                {navItem.label}
              </Box>
            </PopoverTarget>

            {navItem.children && (
              <PopoverDropdown
                p={4}>
                <Stack>
                  {navItem.children.map((child) => (
                    <DesktopSubNav key={child.label} {...child} />
                  ))}
                </Stack>
              </PopoverDropdown>
            )}
          </Popover>
        </Box>
      ))}
    </Stack>
  )
}

