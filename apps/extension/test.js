function wait(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms))
}

async function handleFirstElement(element) {
  simulateClick(element)
  await wait(200)
  const alert = document.querySelectorAll('.PT_outerWrapper_5-109-0')
  if (alert && alert.length > 0) {
    const text = alert[0].querySelector('.CBX_textWrapper_5-109-0')
    if (text) {
      simulateClick(text)
      await wait(50)
    }
    const btn = alert[0].querySelector('.BTN_outerWrapper_5-109-0.BTN_danger_5-109-0')
    if (btn) {
      simulateClick(btn)
      await wait(100)
    }
  }
}

async function clickElement(element) {
  simulateClick(element)
  await wait(300)
}

function simulateClick(element) {
  const clickEvent = new MouseEvent('click', {
    view: window,
    bubbles: true,
    cancelable: true,
  })

  element.dispatchEvent(clickEvent)
}

async function handleElements() {
  const elements = document.querySelectorAll('.RD_textWrapper_5-109-0')
  const doms = Array.from(elements).filter((el) => el.textContent.trim() === '我不接受')
  await handleFirstElement(doms[0])
  for (let i = 1; i < doms.length; i++) {
    await clickElement(doms[i])
    console.log(`点击了第${i}个元素`, doms[i])
  }
}

