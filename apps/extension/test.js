function removeNonMatchingParentDivs(divArray, className) {
  var parentDivs = []
  divArray.forEach((parentDiv) => {
    if (!parentDiv.querySelector(`.${className}`)) {
      parentDivs.push(parentDiv)
    }
  })
  return parentDivs
}

