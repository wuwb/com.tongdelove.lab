function findParentDivsWithClass(divArray, className) {
  const parentDivs = []
  divArray.forEach((parentDiv) => {
    const childDivs = parentDiv.querySelectorAll('div')
    childDivs.forEach((childDiv) => {
      if (childDiv.classList.contains(className)) {
        parentDivs.push(parentDiv)
        return
      }
    })
  })
  return parentDivs
}

